
print("CAlCULATOR")
num_1 = input ("Enter First Number: ")
math = input("Choose Your Equation: ")
num_2 = input("Enter Second Number: ")
if math == "/":
    print(int(num_1) / int(num_2))
if math == "*":
    print(int(num_1) * int(num_2))
if math == "+":
    print(int(num_1) + int(num_2))
if math == "-":
    print(int(num_1) - int(num_2))


